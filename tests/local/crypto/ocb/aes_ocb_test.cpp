void test1() {
    printf("Test 1, auth empty\n\n");

    unsigned char key[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F};
    unsigned char nonce[12] = {0xBB, 0xAA, 0x99, 0x88, 0x77, 0x66, 0x55, 0x44, 0x33, 0x22, 0x11, 0x0F};
    unsigned char ptx[40] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10, 0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1A, 0x1B, 0x1C, 0x1D, 0x1E, 0x1F, 0x20, 0x21, 0x22, 0x23, 0x24, 0x25, 0x26, 0x27};

    unsigned char ctx[40];
    unsigned char tag[16];

    unsigned char ctx_result[40] = {0x44, 0x12, 0x92, 0x34, 0x93, 0xC5, 0x7D, 0x5D, 0xE0, 0xD7, 0x00, 0xF7, 0x53, 0xCC, 0xE0, 0xD1, 0xD2, 0xD9, 0x50, 0x60, 0x12, 0x2E, 0x9F, 0x15, 0xA5, 0xDD, 0xBF, 0xC5, 0x78, 0x7E, 0x50, 0xB5, 0xCC, 0x55, 0xEE, 0x50, 0x7B, 0xCB, 0x08, 0x4E};
    unsigned char tag_result[16] = {0x47, 0x9A, 0xD3, 0x63, 0xAC, 0x36, 0x6B, 0x95, 0xA9, 0x8C, 0xA5, 0xF3, 0x00, 0x0B, 0x14, 0x79};

    AesOcb ocb(key);
    ocb.setNonce(nonce);
    ocb.encryptAndComputeTag(tag, ctx, ptx, 40, nullptr, 0);

    bool success = true;
    for(int i=0; i<40; i++) {
        printf("0x%x ",ctx[i]);
        if(ctx[i] != ctx_result[i]) success = false;
    }
    printf("\n\n");
    if(success) printf("Encryption success!\n\n\n");
    else printf("Encryption FAIL :(\n\n\n");

    success = true;
    for(int i=0; i<16; i++) {
        printf("0x%x ",tag[i]);
        if(tag[i] != tag_result[i]) success = false;
    }
    printf("\n\n");
    if(success) printf("Tag success!\n\n\n");
    else printf("Tag FAIL :(\n\n\n");


    unsigned char ptx_test[40];
    ocb.setNonce(nonce);
    bool valid = ocb.verifyAndDecrypt(tag_result, ptx_test, ctx_result, 40, nullptr, 0);

    success = true;
    printf("\n\n");
    for(int i=0; i<40; i++) {
        printf("0x%x ",ptx_test[i]);
        if(ptx[i] != ptx_test[i]) success = false;
    }
    printf("\n\n");
    if(success) printf("Decryption success!\n\n\n");
    else printf("Decryption FAIL :(\n\n\n");

    if(valid) printf("Verify success!\n\n\n");
    else printf("Verify FAIL :(\n\n\n");


}

